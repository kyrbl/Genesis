package firstLesson;

public class Arithmetic {
    public static void main(String[] args){

        int result = 4 * 10;
        System.out.println(result);

        int a = 4;
        result = result / a;
        System.out.println(result);

        int b = 5;
        System.out.println(b % 2);

        int c = 1;
        c++;
        System.out.println(c);
        c--;
        System.out.println(c);
    }
}
